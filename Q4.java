class Q4{
	public static void main(String[]args){
	Descending();
	}
	// Method to call descending print function
	
	
	
	
		// Method to print numbers from 20 to 4 in descending order
	static void Descending(){
		int i=20;
		while(i>=4);
		{
			System.out.println(i);
			i--;
		}
	}
}


